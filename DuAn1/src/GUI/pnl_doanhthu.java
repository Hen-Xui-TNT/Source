    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        switch (check) {
            case 1:
                    BLL_thongke.DL_Thu(txt_NV.getText(),txt_tungay.getText(), txt_denngay.getText(),  tbl_DThu);
                    if (tbl_DThu.getRowCount() != 0) {
                    txt_tongDthu.setText(ChuyenDoi_ThongBao.TinhTongTien(tbl_DThu, 3));
                    }
                break;
            case 2:
                    BLL_thongke.DL_Chi(txt_NV.getText(),txt_tungay.getText(), txt_denngay.getText(),  tbl_DThu);
                    if (tbl_DThu.getRowCount() != 0) {
                    txt_tongDthu.setText(ChuyenDoi_ThongBao.TinhTongTien(tbl_DThu, 3));
                    }
                break;
            
            default:
                    BLL_thongke.DL_Thu_Chi(txt_NV.getText(),txt_tungay.getText(), txt_denngay.getText(),  tbl_DThu);
                    if (tbl_DThu.getRowCount() != 0) {
                    txt_tongDthu.setText(ChuyenDoi_ThongBao.TinhTongTienDT(tbl_DThu, 4));
                    }
                break;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         if (tbl_DThu.getRowCount() > 0) {
            JFileChooser chooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Tệp excel", "xlsx");
            chooser.setFileFilter(filter);
            chooser.setDialogTitle("Lưu tệp");
            chooser.setAcceptAllFileFilterUsed(false);
            if (chooser.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
                List<JTable> tb = new ArrayList<JTable>();
                List<String> nom = new ArrayList<String>();
                tb.add(tbl_DThu);
                nom.add("Người dùng");
                String file = chooser.getSelectedFile().toString().concat(".xls");
                try {
                    Exporter e = new Exporter(new File(file), tb, nom);
                    if (e.export()) {
                        JOptionPane.showMessageDialog(null, "File vừa chọn đã được xuất ra file Excel", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Xuất hiện lỗi" + e.getMessage(), " Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Không có dữ liệu để xuất","Thông báo lỗi",JOptionPane.ERROR_MESSAGE);
        }  
    }//GEN-LAST:event_jButton1ActionPerformed


